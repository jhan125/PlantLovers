import Foundation
import SwiftUI

extension Color {
    struct  MyTheme {
        static var DarkGreen: Color {
            return Color("DarkGreen")
        }
        static var LightGreen: Color {
            return Color("LightGreen")
        }
        static var Green: Color {
            return Color("Green")
        }
        static var YellowGreen: Color {
            return Color("YellowGreen")
        }
        static var Light: Color {
            return Color("Light")
        }
    }
}
